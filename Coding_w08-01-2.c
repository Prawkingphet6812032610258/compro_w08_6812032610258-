#include <stdio.h>

int main() {
    int i;

    // ใช้ for loop
    for (i = 2; i <= 10; i += 2) {
        printf("%d ", i); // แสดงค่าของ i
    }
    printf("\nEnd of loop (for)\n");

    return 0;
}
//อธิบายการทำงาน:
//for loop:
//   - เริ่มต้น i = 2
//   - ตรวจสอบเงื่อนไข (i <= 10) ถ้าจริงจะแสดงค่า i แล้วบวกเพิ่มทีละ 2
//   - ทำซ้ำจนกว่า i จะมากกว่า 10
//   - โครงสร้างของ for จะรวมการกำหนดค่าเริ่มต้น, เงื่อนไข, และการเพิ่มค่าในบรรทัดเดียวกัน